while (true)
{
    string[] words = RandomWord();

    Random random = new();
    string wordToGuess = words[random.Next(words.Length)];

    char[] guessedWord = new string('_', wordToGuess.Length).ToCharArray();
    int attemptsLeft = 10;
    bool isWordGuessed = false;

    Console.WriteLine("Добро пожаловать в игру 'Виселица'!");
    Console.WriteLine($"Слово состоит из {wordToGuess.Length} букв. У вас есть {attemptsLeft} попыток.\n");

    while (attemptsLeft > 0 && !isWordGuessed)
    {
        Console.WriteLine($"Слово: {new string(guessedWord)}");

        char guess;
        while (true)
        {
            Console.Write("Введите букву: ");
            guess = Console.ReadKey().KeyChar;
            Console.WriteLine();

            if (char.IsLetter(guess))
            {
                guess = char.ToLower(guess);
                break;
            }
            else
            {
                Console.WriteLine("Ошибка: Введите только букву!\n");
            }
        }

        if (wordToGuess.Contains(guess))
        {
            for (int i = 0; i < wordToGuess.Length; i++)
            {
                if (wordToGuess[i] == guess)
                {
                    guessedWord[i] = guess;
                }
            }
            Console.ForegroundColor = ConsoleColor.Green;
            Console.WriteLine($"Верно! Буква '{guess}' есть в слове.\n");
            Console.ResetColor();
        }
        else
        {
            attemptsLeft--;
            Console.ForegroundColor = ConsoleColor.Red;
            Console.WriteLine($"Неверно. Буквы '{guess}' нет в слове. Осталось попыток: {attemptsLeft}.\n");
            Console.ResetColor();
        }

        isWordGuessed = new string(guessedWord) == wordToGuess;
    }

    if (isWordGuessed)
    {
        Console.WriteLine($"Поздравляем! Вы угадали слово: {wordToGuess}\n");
    }
    else
    {
        Console.WriteLine($"Вы проиграли. Загаданное слово было: {wordToGuess}\n");
    }
}


